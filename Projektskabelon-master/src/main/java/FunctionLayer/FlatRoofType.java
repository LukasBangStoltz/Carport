package FunctionLayer;

public class FlatRoofType {

    private int flatRoof_id;
    private String flatRoof_type;

    public FlatRoofType(int flatRoof_id, String flatRoof_type) {
        this.flatRoof_id = flatRoof_id;
        this.flatRoof_type = flatRoof_type;
    }

    public int getFlatRoof_id() {
        return flatRoof_id;
    }

    public void setFlatRoof_id(int flatRoof_id) {
        this.flatRoof_id = flatRoof_id;
    }

    public String getFlatRoof_type() {
        return flatRoof_type;
    }

    public void setFlatRoof_type(String flatRoof_type) {
        this.flatRoof_type = flatRoof_type;
    }


}
